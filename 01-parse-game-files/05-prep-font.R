#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Convert a character index into a 2-column matrix of (x,y) coorindates of points
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
char_idx_to_points <- function(char_idx) {
  byte_idx <- (char_idx - 1L) * 8L + 1:8

  bytes <- font_raw[byte_idx]

  bits <- purrr::map(bytes, ~as.integer(intToBits(.x)[8:1]))
  bits <- do.call(rbind, bits)
  bits

  idx <- which(bits == 1)
  coords <- as.data.frame(arrayInd(idx, dim(bits)))
  setNames(coords, c('y', 'x'))
}


#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
# Create a a list of all char points
#~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
char_points <- lapply(seq(96), char_idx_to_points)

char_names <- strsplit(intToUtf8(utf8ToInt(' ') - 1 + seq(96)), '')[[1]]
char_names[[96]] <- 'checkerboard'

names(char_points) <- char_names


